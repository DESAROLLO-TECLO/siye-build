package mx.com.teclo.siye.persistencia.vo.seguimientoOs;

public class EstiloNodosVO {
	
	private String nbStatus;
	private String nbColor;
	private Long nuOrden;
	
	
	
	/**
	 * @return the nbStatus
	 */
	public String getNbStatus() {
		return nbStatus;
	}
	/**
	 * @param nbStatus the nbStatus to set
	 */
	public void setNbStatus(String nbStatus) {
		this.nbStatus = nbStatus;
	}
	/**
	 * @return the nbColor
	 */
	public String getNbColor() {
		return nbColor;
	}
	/**
	 * @param nbColor the nbColor to set
	 */
	public void setNbColor(String nbColor) {
		this.nbColor = nbColor;
	}
	/**
	 * @return the nuOrden
	 */
	public Long getNuOrden() {
		return nuOrden;
	}
	/**
	 * @param nuOrden the nuOrden to set
	 */
	public void setNuOrden(Long nuOrden) {
		this.nuOrden = nuOrden;
	}	

}
